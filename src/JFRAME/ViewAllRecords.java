    public void search(){
          Date uFromDate = ID1.getDatoFecha();
          Date uToDate = DD1.getDatoFecha();
          
          long l1 = uFromDate.getTime();
          long l2 = uToDate.getTime();
          
          java.sql.Date fromDate = new java.sql.Date(l1);
          java.sql.Date toDate = new java.sql.Date(l2);
        
          try {
              Connection con = DBconnection.getConnection();
              String sql = "select  *  from issue_book  where issue_date  between  ? and  ?";
             PreparedStatement pst1 = con.prepareStatement(sql);
              pst1.setDate(1, fromDate);
              pst1.setDate(2, toDate);
              ResultSet rs = pst1.executeQuery();
              
              
              if(rs.next() == false){
                  JOptionPane.showMessageDialog(this,"No Record Found");
              }else{
                  
                   while(rs.next())
              {
                   int issueId = rs.getInt("id");
                String bookName = rs.getString("book_name");
                String studentName = rs.getString("student_name");
                String issueDate = rs.getString("issue_date");
                String dueDate = rs.getString("due_date");
                String status = rs.getString("status");
                
               Object[] obj = {issueId,bookName,studentName,issueDate,dueDate,status};
                model = (DefaultTableModel) tbl_issuedetails.getModel(); 
                model.addRow(obj);
              }
              }
                  
             
        } catch (Exception e) {
            
        }
              
        
    }
