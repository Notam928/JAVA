    public void setViewAllRecordToTable(){
        
        try {
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_ms","root","");
            Statement sta = con.createStatement();
            ResultSet rs = sta.executeQuery("select * from issue_book");
           
            while(rs.next()){
                int issueId = rs.getInt("id");
                String bookName = rs.getString("book_name");
                String studentName = rs.getString("student_name");
                String issueDate = rs.getString("issue_date");
                String dueDate = rs.getString("due_date");
                String status = rs.getString("status");
                
               Object[] obj = {issueId,bookName,studentName,issueDate,dueDate,status};
                model = (DefaultTableModel) tbl_issuedetails.getModel(); 
                model.addRow(obj);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
    }
    
    
